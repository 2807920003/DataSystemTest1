package com.csu.entity;

public class ImageScreeningInfor {
    private String mrNo;
    private String pName;
    private String imageId;
    private String imageType;
    private String imagePart;
    private String algorithmName;
    private String screeningTaskName;
    private String screeningResult;

    public String getMrNo() {
        return mrNo;
    }

    public void setMrNo(String mrNo) {
        this.mrNo = mrNo;
    }

    public String getPName() {
        return pName;
    }

    public void setPName(String pName) {
        this.pName = pName;
    }

    public String getImageId() {
        return imageId;
    }

    public void setImageId(String imageId) {
        this.imageId = imageId;
    }

    public String getImageType() {
        return imageType;
    }

    public void setImageType(String imageType) {
        this.imageType = imageType;
    }

    public String getImagePart() {
        return imagePart;
    }

    public void setImagePart(String imagePart) {
        this.imagePart = imagePart;
    }

    public String getAlgorithmName() {
        return algorithmName;
    }

    public void setAlgorithmName(String algorithmName) {
        this.algorithmName = algorithmName;
    }

    public String getScreeningTaskName() {
        return screeningTaskName;
    }

    public void setScreeningTaskName(String screeningTaskName) {
        this.screeningTaskName = screeningTaskName;
    }

    public String getScreeningResult() {
        return screeningResult;
    }

    public void setScreeningResult(String screeningResult) {
        this.screeningResult = screeningResult;
    }
}
